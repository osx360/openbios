static unsigned int screen_width;
static unsigned int screen_height;

static unsigned int current_x = 10;
static unsigned int current_y = 10;
static unsigned int init = 0;

static inline void
console_pset32(unsigned int x, unsigned int y, unsigned int color)
{
	unsigned int *fb = (unsigned int*)XBOX360_FB_BASE;
	int base = (((y >> 5)*32*screen_width + ((x >> 5)<<10) + (x&3) + ((y&1)<<2) + (((x&31)>>2)<<3) + (((y&31)>>1)<<6)) ^ ((y&8)<<2));

	fb[base] = __cpu_to_le32(color);
}

static void
xbox360_clearscreen(void)
{
	for (unsigned int y = 0; y < screen_height; y++) {
		for (unsigned int x = 0; x < screen_width; x++) {
			console_pset32(x, y, 0x00000000);
		}
	}
}

static int
xbox360_putchar(int c)
{
	const unsigned char *char_ptr;
	unsigned char v;

	if (!init) {
		// TODO: Get resolution dynamically.
		screen_width = 1280;
		screen_height = 720;
		xbox360_clearscreen();
		init = 1;
	}

	if (c == '\n') {
		current_y += 8;
		current_x = 10;
		return c;
	}

	if (current_y >= (screen_height - 10)) {
    	xbox360_clearscreen();
		current_y = 10;
	}

	//
	// Only handle ASCII characters.
	//
	if ((c < 32) || (c >= 127)) {
		return c;
	}
	c -= 32;

	//
	// Draw character pixels starting at specified location.
	//
  	char_ptr = &gDebugFont[(CHAR_SIZE_X * CHAR_SIZE_Y * c) / 8];

  	for (int i = 0; i < CHAR_SIZE_Y; i++) {
		v = *(char_ptr++);

		for (int j = 0; j < CHAR_SIZE_X; j++) {
			if(v & (128 >> j))
				console_pset32(current_x + j, current_y + i, 0xFFFFFFFF);
			else
				console_pset32(current_x + j, current_y + i, 0x00000000);
		}
	}

	current_x += CHAR_SIZE_X;
  	return c;
}

static int
xbox360_availchar(void)
{
	return 0;
}

static int
xbox360_getchar(void)
{
	return 0;
}

struct _console_ops xbox360_console_ops = {
	.putchar = xbox360_putchar,
	.availchar = xbox360_availchar,
	.getchar = xbox360_getchar
};

